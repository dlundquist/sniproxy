static struct test_packet good[] = {
    { (char *)good_data_1, sizeof(good_data_1), NTYPE_HOST, "localhost", 9 },
    { (char *)alpn_good_data_1, sizeof(alpn_good_data_1), NTYPE_ALPN, "http/2.0", 8 }
};

static struct test_packet bad[] = {
    { (char *)bad_data_1, sizeof(bad_data_1), 0, "localhost"},
    { (char *)bad_data_2, sizeof(bad_data_2), 0, "localhost"}
};

int main() {
    unsigned int i;
    int result;
    struct ProtocolRes res;
    struct Listener l;

    memset(&l, 0, sizeof(l));
    l.prefer_alpn = 1;

    for (i = 0; i < sizeof(good) / sizeof(struct test_packet); i++) {
        memset(&res, 0, sizeof(res));

        result = tls_protocol->parse_packet(&l, good[i].packet, good[i].len, &res);

        assert(result == good[i].expected_res);

        assert(NULL != res.name);

        assert(good[i].expected_type == res.name_type);

        assert(0 == strcmp(good[i].expected_name, res.name));

        free(res.name);
    }

    for (i = 0; i < sizeof(bad) / sizeof(struct test_packet); i++) {
        memset(&res, 0, sizeof(res));

        result = tls_protocol->parse_packet(&l, bad[i].packet, bad[i].len, &res);

        // parse failure or not "localhost"
        assert(result < 0 ||
               res.name == NULL ||
               strcmp(bad[i].expected_name, res.name) != 0);

        free(res.name);
    }

    return 0;
}

